# Example: Using lists in Python
# Lists allow heterogeneous data, meaning they can store multiple data types in the same list.
# They are similar to arrays but not identical (arrays are typically homogeneous in nature).

x = [1, 2, 3, 45, 444.5, 'Sheikh Uzair Raza', True]  # Example of a list with heterogeneous data

# Printing the list
print(x)

# Printing the type of the variable to confirm it's a list
print(type(x))
