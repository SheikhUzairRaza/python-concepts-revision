# Example: Accessing elements in a Python list

# Defining a list with heterogeneous data
a = [55, 43, "SUR", True]

# Printing the entire list
print(a)

# Accessing the first element of the list
print(a[0])  # Output: 55 (first element)
print(type(a[0]))  # Checking the type of the first element

# Accessing the last element of the list using negative indexing
print(a[-1])  # Output: True (last element)
print(type(a[-1]))  # Checking the type of the last element