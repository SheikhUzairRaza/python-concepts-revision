# The first point to note: The `print` function prints each statement on a separate line by default.

print('Single quotes are allowed for strings.')
print("Double quotes are also allowed for strings.")

# If you want to print two `print` statements on the same line, 
# you can use the `end` parameter to avoid the default new line.

print("Hello", end=" ")
print("World")

